namespace App\Doctrine\DBAL;

use Doctrine\ORM\Query\AST\Functions\FunctionNode;
use Doctrine\ORM\Query\Lexer;
use Doctrine\ORM\Query\Parser;
use Doctrine\ORM\Query\SqlWalker;


/**
 * Class FirstValueFunction
 * @package App\Doctrine\DBAL
 */
class FirstValueFunction extends FunctionNode
{
    /**
     * @var
     */
    public $value;

    /**
     * @param Parser $parser
     * @throws \Doctrine\ORM\Query\QueryException
     */
    public function parse(Parser $parser)
    {
        $parser->match(Lexer::T_IDENTIFIER);
        $parser->match(Lexer::T_OPEN_PARENTHESIS);
        $this->value = $parser->StringPrimary();
        $parser->match(Lexer::T_CLOSE_PARENTHESIS);
    }

    /**
     * @param SqlWalker $sqlWalker
     * @return string
     */
    public function getSql(SqlWalker $sqlWalker)
    {
        return 'FIRST_VALUE(' . $this->value->dispatch($sqlWalker) . ') OVER (ORDER BY ' . $this->value->dispatch($sqlWalker) . ')';
    }
}